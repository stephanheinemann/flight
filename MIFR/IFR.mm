    </p>
    <p>
      
    </p>
    <ul>
      <li>
        1&#160;/ 60 rule

        <ul>
          <li>
            1 degree DA corresponds to approximately 1 ft / 60 ft
          </li>
          <li>
            <b>1 NM = 6076.12 ft</b>
          </li>
          <li>
            <b>1 degree DA corresponds to approximately 100 ft / NM</b>
          </li>
          <li>
            descent gradient <b>DG </b>[ft / NM]<b>&#160;= DA * 100</b>
          </li>
          <li>
            DA = 4 deg -&gt; DG = 400 ft / NM
          </li>
          <li>
            DA = 6.5 deg -&gt; DG = 650 ft / NM
          </li>
        </ul>
      </li>
      <li>
        constant descent angle (CDA)

        <ul>
          <li>
            <b>VS = DA * 100 * GS / 60</b>
          </li>
          <li>
            DA = 3 deg, GS = 120 -&gt; 600 ft / min
          </li>
          <li>
            DA = 4 deg, GS = 120 -&gt; 800 ft / min
          </li>
        </ul>
      </li>
      <li>
        <b>3</b>&#160;degrees constant descent angle (CDA)

        <ul>
          <li>
            <b>VS = GS / 2 * 10</b>
          </li>
          <li>
            GS = 120 kts -&gt; VS = 600 ft / min
          </li>
          <li>
            <b>VS = GS * 5</b>
          </li>
        </ul>
      </li>
      <li>
        distance to commence a CDA at <b>3</b>&#160;degrees

